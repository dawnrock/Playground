const dias = "viernes";
console.log(dias);

let asignatura;

switch(dias){
    case "lunes":
        asignatura = "Matemáticas"; break;
    case "martes":
        asignatura = "Lengua"; break;
    case "miercoles":
        asignatura = "Física"; break;
    case "jueves":
        asignatura = "Química"; break;    
    case "viernes":
        asignatura = "Gimnasia"; break;            
}

console.log(asignatura)

